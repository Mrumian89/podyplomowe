public class Test8 {
    public static void main(String[] args) {

        char grade = 'A';

        switch (grade) {
            case 'A':
                System.out.println("Wspaniale!");
                break;

            case 'B':
                System.out.println("Dobrze!");
                break;

            case 'C':
                System.out.println("Mogło być lepiej");
                break;

            default:
                System.out.println("Rozważ oszukiwanie");

        }

    }
}
